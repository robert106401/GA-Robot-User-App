const fs = require("fs");
const path = require("path");

const projectRoot = path.resolve(__dirname, "..");
const packageJsonPath = path.join(projectRoot, "package.json");
const outputPath = path.join(projectRoot, "src", "version.ts");
const packageJson = JSON.parse(fs.readFileSync(packageJsonPath, "utf8"));

const buildId = formatBuildTimestamp(new Date());
const version = packageJson.version;
const contents = `// Generated by scripts/generate-app-version.js. Do not edit by hand.
export const APP_VERSION = "${version}";
export const APP_BUILD = "${buildId}";
export const APP_VERSION_TEXT = "Version ${version}";
export const APP_BUILD_TEXT = "Build ${buildId}";
export const APP_VERSION_LABEL = "Version ${version} · Build ${buildId}";
export const APP_SHORT_VERSION_LABEL = "v${version}";
`;

fs.writeFileSync(outputPath, contents);
console.log(`Generated src/version.ts (${version}, ${buildId})`);

function formatBuildTimestamp(date) {
  const parts = new Intl.DateTimeFormat("en-CA", {
    timeZone: "Asia/Shanghai",
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
    hour: "2-digit",
    minute: "2-digit",
    second: "2-digit",
    hour12: false
  }).formatToParts(date);
  const values = Object.fromEntries(parts.map((part) => [part.type, part.value]));
  return `${values.year}${values.month}${values.day}${values.hour}${values.minute}${values.second}`;
}
